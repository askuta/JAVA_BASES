package com.java.reincarnator;

public class Embodiment {

	public String body;
	public String clothe;
	public String eyes;
	public String hair;
	public String hand;
	public String head;
	public String mouth;
	public String nose;

	public Embodiment(String body, String clothe, String eyes, String hair, String hand, String head, String mouth,
			String nose) {
		this.body = body;
		this.clothe = clothe;
		this.eyes = eyes;
		this.hair = hair;
		this.hand = hand;
		this.head = head;
		this.mouth = mouth;
		this.nose = nose;
	}

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}

	public String getClothe() {
		return clothe;
	}

	public void setClothe(String clothe) {
		this.clothe = clothe;
	}

	public String getEyes() {
		return eyes;
	}

	public void setEyes(String eyes) {
		this.eyes = eyes;
	}

	public String getHair() {
		return hair;
	}

	public void setHair(String hair) {
		this.hair = hair;
	}

	public String getHand() {
		return hand;
	}

	public void setHand(String hand) {
		this.hand = hand;
	}

	public String getHead() {
		return head;
	}

	public void setHead(String head) {
		this.head = head;
	}

	public String getMouth() {
		return mouth;
	}

	public void setMouth(String mouth) {
		this.mouth = mouth;
	}

	public String getNose() {
		return nose;
	}

	public void setNose(String nose) {
		this.nose = nose;
	}

}
